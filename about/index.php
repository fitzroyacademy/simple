<h1>A little about us and this project.</h1>

<p>Fitzroy Academy is a project created by Will Dayble and Avraham Vofsi along with many collaborators and partners over the past 6 years.</p>

<p>Initially conveived as an online social entreprenuership school alternative to higher education, through many iterations landed on this: An online repository for some free <i>kick-ass</i> lessons around entreprenurship, social impact, digital skills and online education.</p>

<p>The resources are free to use within your own programs, organizations and projects. Each lesson is between 30 minutes and 1 hour of video as well as a template or worksheet activity to create and some completed example. Each lesson represents a few hours work by yourself, or even better with a group of people, a half day workshop.</p>

