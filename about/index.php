	<h3 class="green">The timeline so far...</h3>

	<dl class="fat_storyline">
		<dt>2012-ish</dt>
		<dd><a href="https://www.linkedin.com/in/lukegiuliani/" target="_blank">Luke</a>, <a href="https://www.linkedin.com/in/willdayble/" target="_blank">Will</a> and Jules start a <a href="http://gracefitzroy.com/" target="_blank">cafe in Fitzroy</a>, and use the back few rooms as studio space for their <a href="https://squareweave.com.au" target="_blank">digital agency</a>.</dd>
		<dt>2013</dt>
		<dd>In the back meeting room of the studio, we run tiny courses for friends and colleagues after work hours. Students pay $5 per evening to one of our staff members who'd stick around after work to mentor and teach.</dd>
		<dt>2014</dt>
		<dd>Will gets <a href="https://medium.com/business-as-usual/the-moment-i-realised-university-doesnt-work-6352bc7be446" target="_blank">all worked up about universities</a>, and we launch the 'Fitzroy Academy of Getting Shit Done', an impact accelerator for non-techy people. We pilot a program with 25 students and 40 teachers + mentors at <a href="http://donkeywheelhouse.org/">Donkey Wheel house</a>. It's a roaring success.</dd>
		<dt>2015</dt>
		<dd>We do the numbers and realise that physical programs will be far too expensive for most of the folks we want to help, so we decide to play around with going completely online. We make <a href="/about/mistakes">many, many mistakes.</a></dd>
		<dt>2016</dt>
		<dd>The <a href="https://exchange.telstra.com.au/telstra-news/telstra-foundation/" target="_blank">Telstra Foundation</a> comes on board as our first serious partner, sponsoring 20 video lessons on digital and user experience for the non-profit sector. We start selling social entrepreneurship education on the internet.</dd>
		<dt>2017-18</dt>
		<dd>We film lessons with the <a href="https://mulagofoundation.org/" target="">Mulago Foundation</a> crew and their fellows on high impact systems change, which is <em>tremendously</em> exciting. Will starts teaching at Monash University.</dd>
		<dt>2019</dt>
		<dd>The <a href="https://www.firetree.org/" target="_blank">Firetree Trust</a> invite us to do work in South East Asia, which turns into child protection policy work with <a href="https://www.stairwayfoundation.org/" target="_blank">Stairway</a> in the Phillipines and women-led micro-enterprise with <a href="https://www.sheinvestments.com/" target="_blank">SHE Investments</a> in Cambodia.</dd>
		<dt>2020</dt>
		<dd>After 5 years, we <a href="https://medium.com/fitzroy-academy/learn-online-not-alone-671508b0dc79" target="_blank">give up on platform education</a>. COVID-19 hits. We start pouring more time and energy into our own <a href="/courses/">online courses</a>, building our own LMS tech and <a href="/services">helping other organisations</a> with blended learning.</dd>
		<dt>2021+</dt>
		<dd>We still have no rock solid plan, but we do have a better idea of where the gaps in blended, distributed learning are. We feel a big missing piece is in political activism and policy work, and the structures around how communities gather together to organise themselves around collective issues.</dd>
	</dl>
